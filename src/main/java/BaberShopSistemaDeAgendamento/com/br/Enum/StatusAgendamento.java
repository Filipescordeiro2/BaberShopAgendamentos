package BaberShopSistemaDeAgendamento.com.br.Enum;

/**
 * Enumeração representando os possíveis status de um agendamento.
 */
public enum StatusAgendamento {

    /**
     * Status indicando que o agendamento está marcado e confirmado.
     */
    AGENDADO,

    /**
     * Status indicando que o agendamento foi cancelado.
     */
    CANCELADO,

    /**
     * Status indicando que o agendamento foi concluído com sucesso.
     */
    CONCLUIDO;
}